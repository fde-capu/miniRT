#include "libft.h"

int	ft_isnumber(char *str)
{
	char	*p;

	p = str;
	while ((*p) && ((*p == ' ') || (*p == '-') || (*p == '+')))
		p++;
	while ((*p) && (*p >= '0') && (*p <= '9'))
		p++;
	return (*p ? 0 : 1);
}
