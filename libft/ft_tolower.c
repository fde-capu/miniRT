#include "libft.h"

int		ft_tolower(int c)
{
	return (c >= 'A' && c <= 'Z' ? c + ('a' - 'A') : c);
}

int		ft_islower(int c)
{
	return (c >= 'a' && c <= 'z' ? 1 : 0);
}
