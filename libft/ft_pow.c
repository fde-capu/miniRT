#include "libft.h"

double	ft_pow(double a, int e)
{
	if (e == 0)
		return (1.0);
	if (e > 1)
		return (a * ft_pow(a, e - 1));
	if (e < -1)
		return (a / ft_pow(a, e + 1));
	if (e == 1)
		return (a);
	return (1.0);
}
